"""
jump statements..

break - it terminates the loop

continue - it terminates/skips the iteration

"""

# Example with 'break'
print("Example with 'break':")
for i in range(10):
    if i == 5:
        break  # terminates the loop when i is 5
    print(i, end=" ")
print()  # for newline

# Example with 'continue'
print("Example with 'continue':")
for i in range(10):
    if i == 5:
        continue  # skips the iteration when i is 5
    print(i, end=" ")
print()  # for newline



