# Prompt the user to input two values
x = float(input("Enter the 1st value: "))
y = float(input("Enter the 2nd value: "))


# Perform arithmetic operations
sum_result = x + y
print(f"Sum of {x} and {y}: {sum_result}")  # Addition

sub_result = x - y
print(f"Subtraction of {x} and {y}: {sub_result}")  # Subtraction

mul_result = x * y 
print(f"Product of {x} and {y}: {mul_result}")  # Multiplication

div_result = x / y
print(f"Division of {x} by {y}: {div_result}")  # Division

flrdiv_result = x // y
print(f"Floor Division of {x} by {y}: {flrdiv_result}")  # Floor Division

rem_result = x % y
print(f"Remainder of {x} divided by {y}: {rem_result}")  # Modulus
