# Comparison operators...

# Equality
print("Is 10 equal to 20?", 10 == 20)  # Output: False
print("Is 10 equal to 10?", 10 == 10)  # Output: True

# Inequality
print("Is 10 not equal to 20?", 10 != 20)  # Output: True
print("Is 10 not equal to 10?", 10 != 10)  # Output: False

print()

# Less than, Greater than
print("Is 10 less than 20?", 10 < 20)  # Output: True
print("Is 10 greater than 20?", 10 > 20)  # Output: False
print("Is 10 less than or equal to 20?", 10 <= 20)  # Output: True
print("Is 10 greater than or equal to 10?", 10 >= 10)  # Output: True

print()

# Arithmetic operations followed by comparison
print("Is 10+20 less than 40?", 10+20 < 40)  # Output: True
print("Result of 10-40+30:", 10-40+30)  # Output: 0
