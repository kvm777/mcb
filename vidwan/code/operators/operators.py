# Arithmetic operators..

x = 300
y = 11

sum = x + y
print("sum:", sum)  # Addition

sub = x - y
print("subtract:", sub)  # Subtraction

mul = x * y 
print("product:", mul)  # Multiplication

div = x / y
print("division:", div)  # Division

flrdiv = x // y
print("floor division:", flrdiv)  # Floor Division

rem = x % y
print("remainder:", rem)  # Modulo
